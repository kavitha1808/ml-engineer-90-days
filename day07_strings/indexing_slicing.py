# Indexing and Slicing in Strings

s = "Python Programming"

# Indexing
print("First character:", s[0])
print("Last character:", s[-1])

# Slicing
print("First 6 characters:", s[0:6])
print("From index 7 to end:", s[7:])
print("Full string using slicing:", s[:])
print("Reverse string:", s[::-1])

# Step slicing
print("Every second character:", s[::2])
